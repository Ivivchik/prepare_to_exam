#include <unistd.h>

void	it_is_space(char *a)
{
	int i;

	i = 0;
	if (a[i] == ' ' || a[i] == '\n' || a[i] == '\t')
		i++;
	while (a[i] != '\0')
		write(1, &a[i++], 1);
}

int		main(int ac, char **av)
{
	char	*a;
	int		i;
	int		j;

	a = av[1];
	i = 0;
	j = 0;
	if (ac == 2)
	{
		while (av[1][i] != '\0')
		{
			if ((av[1][i] == '\n' || av[1][i] == ' ' || av[1][i] == '\t') &&
				(av[1][i + 1] == '\n' || av[1][i + 1] == ' ' ||
				av[1][i + 1] == '\t' || av[1][i + 1] == '\0'))
				i++;
			else
				a[j++] = av[1][i++];
		}
		a[j] = '\0';
		it_is_space(a);
	}
	write(1, "\n", 1);
	return (0);
}
